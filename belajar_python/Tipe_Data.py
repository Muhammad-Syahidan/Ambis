#Tipe data adalah jenis data yang digunakan untuk menyimpan nilai atau informasi dalam program.
#Tipe data yang umum digunakan dalam Python antara lain:   
#1. Integer (int): Tipe data untuk menyimpan bilangan bulat, seperti 1, 2, 3, dll.
#2. Float (float): Tipe data untuk menyimpan bilangan desimal, seperti 3.14, 2.5, dll.
#3. String (str): Tipe data untuk menyimpan teks atau karakter, seperti "Hello", "Python", dll.
#4. Boolean (bool): Tipe data untuk menyimpan nilai benar (True) atau salah (False).
#5. List: Tipe data untuk menyimpan kumpulan nilai yang dapat diubah, seperti [1, 2, 3], ["apple", "banana", "cherry"], dll.
#6. Tuple: Tipe data untuk menyimpan kumpulan nilai yang tidak dapat diubah, seperti (1, 2, 3), ("apple", "banana", "cherry"), dll.
#7. Dictionary (dict): Tipe data untuk menyimpan pasangan kunci-nilai, seperti {"name": "Alice", "age": 30}, dll.
#8. Set: Tipe data untuk menyimpan kumpulan nilai unik, seperti {1, 2, 3}, {"apple", "banana", "cherry"}, dll.
#9. None: Tipe data untuk menyimpan nilai kosong atau tidak ada, seperti None

#Contoh penggunaan tipe data dalam Python:
#Integer
x = 10
print(type(x))  # Output: <class 'int'> 
#Float
y = 3.14
print(type(y))  # Output: <class 'float'>
#String
name = "Alice"
print(type(name))  # Output: <class 'str'>
#Boolean
is_valid = True
print(type(is_valid))  # Output: <class 'bool'>
#List
fruits = ["apple", "banana", "cherry"]
print(type(fruits))  # Output: <class 'list'>
#Tuple 
coordinates = (10, 20)
print(type(coordinates))  # Output: <class 'tuple'>
#Dictionary
person = {"name": "Alice", "age": 30}
print(type(person))  # Output: <class 'dict'>
#Set
unique_numbers = {1, 2, 3}      
print(type(unique_numbers))  # Output: <class 'set'>
#None
value = None
print(type(value))  # Output: <class 'NoneType'>

